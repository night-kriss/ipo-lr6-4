#Написать программу, которая:
#- Запрашивает у пользователя строку для поиска.
#- Находит и выводит все строки, которые содержат искомую подстроку,
#а также их количество из следующего файла.
#Сортирует найденные строки в порядке их длины (от самой короткой к самой длинной) и выводит их.
string=str(input("Введите строку для поиска:"))
# Открываем файл в режиме чтения
with open('text.txt', 'r', encoding='utf-8') as file:   
    # Читаем содержимое файла построчно
    lines = file.readlines()
# Инициализируем переменные для хранения результатов
matching_lines = []
count = 0

# Проходим по каждой строке и проверяем наличие искомой подстроки
for line in lines:
    if string in line:
        matching_lines.append(line.strip())
        count += 1
matching_lines.sort(key=len)        
# Выводим результаты
print("Строки, содержащие искомую подстроку:")
for line in matching_lines:
    print(line)

print(f"\nКоличество строк, содержащих подстроку '{string}': {count}")        
